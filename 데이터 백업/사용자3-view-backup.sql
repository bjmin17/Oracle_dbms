    LEFT JOIN tbl_student ST
        ON SC.s_num = ST.st_num
    LEFT JOIN tbl_dept DP
        ON SC.s_dept = DP.d_num
)
;
--------------------------------------------------------
--  DDL for View VIEW_ST_DEPT
--------------------------------------------------------

  CREATE OR REPLACE FORCE VIEW "VIEW_ST_DEPT" ("학번", "이름", "학과코드", "학과이름", "담임교수") AS 
