  CREATE OR REPLACE FORCE VIEW "VIEW_SCORE" ("S_NUM", "ST_NAME", "S_DEPT", "D_NAME", "D_PRO", "S_KOR", "S_ENG", "S_MATH", "총점", "평균") AS 
  (
SELECT SC.s_num, ST.st_name, SC.s_dept, DP.d_name, DP.d_pro, SC.s_kor, SC.s_eng, SC.s_math,
        SC.s_kor + SC.s_eng + SC.s_math AS 총점,
        ROUND((SC.s_kor + SC.s_eng + SC.s_math) / 3, 0) AS 평균
